<h1>Student Sponsorships</h1>

We are delighted to announce funding for KR 2018 student support. The purpose is to enable students to travel and participate in the conference. To be eligible, the student must be registered as a full-time student at a higher education institution (e.g. University). Students who are already admitted into the KR 2018 Doctoral Consortium are not eligible.<br/><br/>

The support offered consists of: <br/>
<li>The student registration fee for KR 2018 is waved and/or</li>
<li>Shared accommodation (two) at the conference hotel is covered for five nights.</li>

<br/>There are two separate tracks: one for non-US students and another for US students due to different funding sources.<br/><br/>

<b>Application Deadline: August 31, 2018<br/>
Notification: September 5, 2018<br/><br/></b>


