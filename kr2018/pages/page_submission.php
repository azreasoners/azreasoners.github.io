<h1>Submission information</h1>


<p>Submissions must be original, and should not have been previously published, accepted for publication, or currently be under review. Authors may not submit their paper elsewhere during the KR 2018 reviewing period. These considerations apply only to journals and conferences, and not to workshops and forums with a limited audience and without archival proceedings. In case of doubt, please contact the Program Chairs.</p>

<p>Papers must be submitted in AAAI style and PDF format. We invite two kinds of submissions:
<ul class='arrows'>
<li><b>full papers</b> of up to 9 pages including abstract, figures, and appendices (if any) but excluding references and acknowledgements, which may take up to one additional page;  submission of additional material (e.g. proofs) as separate documents is allowed, but this material should not form an integral part of the submission and will only be consulted at the discretion of reviewers, PC members and (area and program) chairs, as appropriate;</li>
<li><b>short papers</b> describing applications, systems and/or demos, of up to 4 
    pages including abstract, figures, and appendices (if any) but excluding 
    references and acknowledgements, which may take up to one additional page.
</li>
</ul>
</p>

<p>
All submissions will be made electronically, through the EasyChair conference system:<br/>
<a href='https://www.easychair.org/conferences/?conf=kr2018' title='EasyChair KR18' target='_blank'><span class="glyphicon glyphicon-link" aria-hidden="true"></span> https://www.easychair.org/conferences/?conf=kr2018</a>
</p>

<p>Papers need <b>not</b> be anonymized (reviewing will be single-blind).</p>

