<h3>Banquet &amp; tours</h3>
<p>
Your registration for KR includes one banquet ticket for KR 2018. Your registration for DL 2018 or NMR 2018 includes one banquet ticket for the joint DL/NMR banquet. If you register for KR 2018 as well as one or both of DL 2018 and NMR 2018, you will receive both tickets, one for each event.
</p>

<p>During the registration, you may select if you're interested in tours to Grand Canyon and Sedona. The fee is <b>not</b> included in the cost above and will need to be paid to the tour company at the time of the tour.</p>




