<h2>9. Canyon De Chelly National Monument</h2>
<p>Canyon de Chelly National Monument is home to spectacular Native American cliff dwellings set along steep-sided canyons, with walls up to 1,000 feet high. In the main canyon, some of the key attractions are the White House Ruins, constructed circa 1050 and discovered in 1849, and Spider Rock. The White House is the best known of the more than one hundred cliff dwellings. The only self-guided hike in the park, which leaves from the White House Overlook on the South Rim, descends 600 feet to the White House Ruins. Other cliff dwellings include the Antelope House and Mummy Cave (in which mummies were found) in the Canyon del Muerto. Most of the ruins, which are largely inaccessible to visitors, were occupied from around AD 350 to 1300.</p>

<p>You can tour the site on your own by driving along the North and South Rim Drives and stopping at the pullouts. Although you can easily see ruins nearby, many of the lookouts also have scopes that let you pinpoint ruins on the distant walls. From spring until fall, you can join a free ranger-led hike or take a private tour of the canyon.</p></br>


<h2>10. Bisbee</h2>
<p>In the far south of Arizona, near the Mexico border, is one of Arizona's most interesting towns and possibly one of its best kept secrets. The former mining town of Bisbee is a unique little community set high in the mountains.</p>

<p>After the mines here closed, Bisbee became a ghost town, and squatters took up residence. Eventually, the town became a haven for artists and hippies. Today, it is a prosperous small town with an eclectic mix of residents, all kinds of unique shops and restaurants, and many interesting things to see and do. Homes, many of which are former miners' cabins, line the hills surrounding the historic downtown area. Many of these residences are only accessible via long sets of stairs that lead to the town center below. Views from the upper streets and hillside homes are incredible, looking out over mountains all the way to Mexico. In the hills above town are a number of informal hiking trails.</p>

<p>Bisbee is a popular day trip from Tucson and surrounding areas, often combined with a stop in Tombstone.</p></br>




<h2>11. Lake Mead National Recreation Area</h2>
<p>Lake Mead National Recreation Area spans 177 miles of the Colorado River and includes Lake Mead and Lake Mohave. The area's 1.5 million acres stretches into southern Nevada.</p>

<p>The area offers boating and water sports, camping facilities, fishing, and hiking opportunities. Lake Mohave is 67 miles long, making it the smaller of the two major lakes in the Lake Mead National Recreation Area. Like its counterpart, Lake Mohave is an artificial body of water, held back by Davis Dam. Willow Beach is a small resort town on the Colorado River with accommodation, restaurants, a marina, and fish hatchery.</p></br>

