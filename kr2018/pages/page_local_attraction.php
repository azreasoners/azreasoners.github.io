<h2>12. Petrified Forest National Park</h2>
<p>Large pieces of petrified wood, along with fossilized plants, fish, and reptiles, have been revealed in great numbers in what is today Petrified National Forest in the Painted Desert. The park access road allows visitors to drive passed many of the highlights and short interpretive trails allow for close up looks at a variety of unique sights. The Visitors Center provides insight into the ecology and geology of the park and is a good place to start before heading out on the drive.</p></br>

<h2>13. Antelope Canyon at Page</h2>
<p>Seen in countless photographs in galleries all over Arizona, Antelope Canyon is a slot canyon located just outside of Page. The sculpted, twisting sandstone walls rise up around you, with shafts of light breaking through from the narrow opening above as you walk through the canyon. Visitors can tour Upper Antelope Canyon on a guided tour, although once you reach the canyon, you are given plenty of free time to explore on your own and at your own pace. The canyon lies within the Navajo Nation reserve boundary.</p></br>
