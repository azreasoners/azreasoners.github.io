<h2>6. Monument Valley</h2>
<p>One of the most iconic images of the Southwest are the sandstone buttes that dominate Monument Valley. This area, straddling the border between Arizona and Utah, includes jagged rock formations, stone spires and buttes, and sand dunes. At the heart of the valley is the Monument Valley Navajo Tribal Park, where you'll find an impressive visitor center and a 17-mile self-drive route along a one-way gravel road through the park. You can also take a guided tour to explore the area more thoroughly. If you don't have time to go into the park, you can appreciate some of the views from the highway.</p></br>

<h2>7. Glen Canyon National Recreation Area</h2>
<p>The Glen Canyon National Recreation Area is a stunning region of blue water, desert landscape, and dramatic stone walls. Home to Lake Powell, one of the largest manmade lakes in the United States, this area is known for both land-based and water-based recreational activities.</p>

<p>The Glen Canyon Dam was constructed between 1956 and 1964 to block the Colorado River and create Lake Powell. There is still a 15 mile section of Glen Canyon downstream from the Glen Canyon Dam, which stretches from the dam down to Lees Ferry.</p>

<p>The town of Page is a good base for exploring the Glen Canyon National Recreation area and surrounding region. The largest marina on Lake Powell, Wahweap Marina, is located just 7 miles north of Page. One of the area's biggest attractions is the nearby slot canyons of Antelope Canyon. Depending on the section of the canyon, visitors can either simply walk through a narrow slot canyon with shafts of light penetrating through the top, illuminating the red walls, or repel down into a canyon. Photos of Antelope Canyon are often found on postcards or in fine art galleries. Visitors can visit Antelope Canyon on a guided tour.</p></br>

<h2>8. Havasu Falls</h2>
<p>Near Supai on the Havasupai Indian Reservation is the 100-foot Havasu Falls. The pools at the base of Havasu Falls have a bluish green tint to the water, and the fall is forked so it appears that there are two falls when the river is flowing heavily. In the canyon of Havasu Creek, a tributary of the Colorado River, some 450 Havasupai Indians (the "people of the blue-green water") live a secluded life, subsisting on their modest farming activities but now mainly dependent on the tourist trade. In this paradisiac valley the Havasu have created a number of waterfalls and carved out basins in the travertine rock which form attractive bathing pools.

Day hiking is not allowed here. Visitors need to make reservations, obtain a permit, and pay a fee. Access is via a long hike, horseback or mule, or helicopter.</p></br>



