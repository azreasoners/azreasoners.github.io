<h2>1. Tempe</h2>
<p>Located just minutes from Phoenix, Tempe boasts lots of things to see and do that both the young, and young at heart, will enjoy. Take advantage of our beautiful weather and head to Tempe Town Lake. Enjoy an invigorating walk, run or bike ride along five miles of shoreline that surrounds the lake. Rent a boat, kayak, stand up paddle board or pedal boat for a leisurely cruise on this two-mile man-made urban oasis.</p>

<p>Explore the beautiful Sonoran Desert in Papago Park. Enjoy the scenery from one of the many hiking paths, or plan a visit to the Phoenix Zoo or the Desert Botanical Garden. At the Desert Botanical Garden, you can wander the cacti lined trails and other arid climate plants from around the globe and listen to concerts under the stars. On Saturdays from May 26-September 1, 7-9:30 p.m., you can see how the desert comes to life when the sun goes down at the Garden during a Desert Botanical Garden Flashlight Tours (bring your own flashlight!).</p>

<p>Visit Arizona Mills mall for great shopping, dining and kid friendly attractions like SEA LIFE Arizona Aquarium where you’ll discover fascinating aquatic creatures. Directly across from SEA LIFE, you’ll find LEGOLAND® Discovery Center  where you can get creative with the thousands of LEGO bricks in themed build and play areas.  Other features include two LEGO rides, a 4D Cinema, massive, large-scale models, building areas and a Model Builder Academy. Tilt Studio at Arizona Mills is a blast from the past where you can play some of the newest video games and old-school pin ball too.</p>

<p>Tempe Marketplace is a gathering place for visitors and locals alike. This shopping and entertainment destination is home to H&M, Old Navy, Nordstrom Rack, Dave & Buster’s, California Pizza Kitchen and a 16-screen Harkins Theatre. Relax with an iced tea while the kids enjoy the Splash Pad. And, on Friday and Saturday evenings, enjoy live music at the District Stage.</p>

<p>Plan a visit to enjoy the pedestrian-friendly tree lined streets of Downtown Tempe. Explore more than 100 hip, inventive, even bohemian, shops, restaurants, taverns and nightclubs, a few that host live and local bands. Looking to add a quick hike to your day? Just steps from Downtown Tempe is “A” Mountain. It’s a steep, but but quick up the mountain with views of Tempe, Scottsdale and Phoenix from the top.</p>

<p>Get wet and wild at Big Surf Waterpark this summer. Arizona’s best waterpark is the best place to spend those hot summer days. Enjoy waterslides, America’s original wave pool at Waikiki Beach or kid friendly water park attractions like Bora Bora Bay or Captain Cook’s Landing.</p>

<p>Arts and cultural attractions abound in Tempe. See a Broadway show at Frank Lloyd Wright designed theatre ASU Gammage, or take in the contemporary art on display at ASU Art Museum. Visit the Tempe Center for the Arts, located on the south side of Tempe Town Lake, and explore the Gallery at TCA, see a show or stay for the live music.</p></br>


<h2>2. Phoenix</h2>
<p>Phoenix is a great base for exploring Arizona but it's also a major destination in the winter for golfers and sun seekers, who simply want to enjoy a stay at a resort or spa and spend a little time basking in the heat of the desert. In the Phoenix Metropolitan Area, which includes Scottsdale and Mesa, you'll find great shopping, dining, golf courses, desert parks for walking, hiking, and biking, and some outstanding attractions. Topping the list of places to visit around Phoenix are the Heard Museum and Frank Lloyd Wright's Taliesin West.</p></br>

