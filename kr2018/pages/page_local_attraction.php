<h2>3. Grand Canyon</h2>
<p>Standing on the rim of the Grand Canyon looking out over the endless ridges of colorful cliff walls and deep ravines, it's impossible not to be inspired by this natural wonder. The canyon walls glow in the late afternoon sun, revealing hues of orange, red, yellow, and everything in between. One of the biggest attractions in America, and certainly in the state of Arizona, the Grand Canyon is nothing less than spectacular. This incredible landscape has been carved out by the Colorado River, seen in the distance far below.

Most visitors see the canyon from the South Rim, where there are numerous lookout areas all along the road, and walkways running along the canyon's edge. The North Rim provides a different view altogether, but the road is closed in winter. For those who want a closer look, it is possible to hike down into the Grand Canyon or take a helicopter flight over and through the canyon.</p></br>


<h2>4. Hoover Dam</h2>
<p>Hoover Dam is one of the world's great engineering marvels. This massive structure, completed in 1935, crosses the Colorado River, linking Arizona and Nevada. It is 726 feet high and 1,244 feet long. Lake Mead, held back by the Hoover Dam, is the largest artificial lake in the United States. It is 110 miles long, and holds the equivalent of two years of flow of the Colorado River.</p>

<p>Visitors can drive or walk across the dam for free, although there is a charge for parking. Another option is to take a tour of Hoover Dam or the Powerplant. On site is the visitor center, with information on the dam and tours, and a café with some basic food options.</p></br>

<h2>5. Sedona</h2>
<p>Surrounded by stunning red rock mountains and buttes, Sedona has one of the most beautiful settings in Arizona. About a 1.5-hour drive north of Phoenix, Sedona is a popular day trip from the city, but the town is worthy of much more than just a few hours. The drive into town from the south, through the Village of Oak Creek, is stunning and offers numerous scenic pullouts.</p>

<p>Sedona's main street (89A) is full of interesting tourist shops, art galleries, and restaurants and is an easy place to spend an afternoon. All around Sedona, you'll find great hiking and mountain biking trails, but Jeep tours, one of the most popular things to do here, provide an even easier way to get out into the landscape. Also in the surrounding area are a number of ancient Native American dwellings, which you can visit on your own or as part of a Jeep tour.</p>

<p>Sedona is considered by many to be a highly spiritual place. It is known for its energy vortexes, found at a number of sites around the town. In downtown, you'll also find New Age shops and unique opportunities, from UFO tours and aura readings to psychics and crystal sellers.</p></br>




