<p>
<b>Workshop/Tutorial dates:</b> 27-29 October 2018</br>
<b>KR main program:</b> 30 October to 21 November 2018</br>
<b>Deadline</b> (for proposal submissions): 21 February 2018
</p>

<p><span class='green'>http://kr2018.org/</span>

<p>For its 2018 edition, KR will solicit proposals for both the Tutorial and Workshop tracks. Tutorials and workshops will be held from 27 to 29 October 2018, prior to the KR main technical program, which will run from 30 October to 1 November 2018. The attendance of tutorials is complimentary to all KR registered participants. Workshop attendance will be subject to payment of a workshop fee, which is separate from that of the main conference.</p>

