<h3>Submission requirements for tutorial proposals</h3>

<p>Each tutorial proposal should contain the following information:</p>

<ul>
<li>A short title of the tutorial.</li>
<li>A two-paragraph description of the tutorial.</li>
<li>Proposed length of the tutorial (half day is the default and recommended length, but an argument can be made for a full day tutorial). </li>
<li>A detailed outline of the tutorial.</li>
<li>The potential target audience for the tutorial and prerequisite knowledge.</li>
<li>A brief resume of the presenter(s) including:
  <ul>
  <li>Name,affiliation, and email address.</li>
  <li>Evidence of scholarship in the area, including a list of publications.</li>
  <li>Evidence of teaching experience.</li>
  </ul>
</ul>

<p>The main duties of the tutorial organizers are:</p>

<ul>
<li>Setup a web-site for the tutorial, which should include, at least, title and abstract of the tutorial, presentersâ€™ details, outline, tutorial notes and related reading material.</li>
<li>Deliver the tutorial at KR 2018.</li>
</ul>


<h3>Submission requirements for workshops proposals</h3>

<p>Each workshop proposal should contain the following information:</p>

<ul>
<li>Title of the workshop and acronym.</li>
<li>Names, affiliations, and contact details of the organisers.</li>
<li>Short description and format.</li>
<li>History of the workshop (if applicable) and related events.</li>
<li>Size of the workshop and duration.</li>
<li>Tentative list of PC members with their respective affiliations.</li>
<li>Experience of the organisers.</li>
<li>Tentative call for papers.</li>
</ul>

<p>The main duties of the workshop chairs are:</p>

<ul>
<li>Set up a website for the workshop.</li>
<li>Advertise the workshop and distribute its call for papers.</li>
<li>Coordinate the peer-reviewing of submitted contributions.</li>
<li>Organise a schedule for the workshop in collaboration with the local organisers and the Workshop Co-Chairs.</li>
<li>Coordinate and moderate the workshop participation and content.</li>
</ul>

<p>Each accepted proposal will be waived two workshop registrations to be used at the discretion of the organisers (e.g., to cover the registration of an invited speaker).</p>

<p>KR reserves the right to cancel a workshop if it does not have enough participants to cover its running costs.</p>


