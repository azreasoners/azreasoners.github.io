
	<p>Welcome to the <b>16th International Conference on Principles of Knowledge
	Representation and Reasoning</b> to be held in Tempe, Arizona (USA) on October 27-November 2, 2018.</p>

	<p>Co-located with <a href='http://www.dcs.bbk.ac.uk/~michael/dl2018/' target='_blank' title='DL 2016'>DL 2018</a> (abstract: July 21, paper: July 28) and
                                      <a href='http://www4.uma.pt/nmr2018/'>NMR 2018</a> (abstract: July 22, paper: July 29)</p>

	<p>Knowledge Representation and Reasoning (KR) is an exciting, well-established
	field of research. In KR a fundamental assumption is that an agent's
	knowledge is explicitly represented in a declarative form, suitable for
	processing by dedicated reasoning engines. This assumption, that much of
	what an agent deals with is knowledge-based, is common in many modern
	intelligent systems. Consequently, KR has contributed to the theory and
	practice of various areas in AI, such as automated planning, natural language
	understanding, among others, as well as to fields beyond AI, including
	databases, verification, and software engineering. In recent years KR has
	contributed to new and emerging fields including the semantic web,
	computational biology, and the development of software agents.</p>


	<p>The <a href='http://www.kr.org' target='_blank' title='kr.org'>KR conference series</a> is a leading forum for timely in-depth presentation of progress in the theory and principles underlying the representation and computational management of knowledge. <a href='http://www.kr.org/' target='_blank' title='KR 2018'>KR 2018</a> will be a forum for the exchange and discussion of new ideas, issues, and results on the principles and practice of
	KR.</p>

